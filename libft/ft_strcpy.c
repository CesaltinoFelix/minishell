#include "libft.h"

char *ft_strcpy(char *dest, const char *src)
{

	if (!src || !dest)
		return (NULL);
	size_t	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}